"""Team Copilot Tests - Integration Tests - Users - Create User."""

from uuid import uuid4
from datetime import datetime, timezone
from unittest.mock import patch

from fastapi import FastAPI, status
from fastapi.testclient import TestClient

from team_copilot.core.auth import get_admin_user
from team_copilot.models.data import User

from tests.integration import raise_not_authorized_exc


def test_create_user(app: FastAPI, test_client: TestClient, test_admin_user: User):
    """Test the "create_user" endpoint.

    Args:
        app (FastAPI): FastAPI application.
        test_client (TestClient): FastAPI test client.
        test_admin_user (User): Mock enabled administrator user.
    """
    # Simulate the injected dependency
    app.dependency_overrides[get_admin_user] = lambda: test_admin_user

    user_id = uuid4()
    now = datetime.now(timezone.utc)

    # Request data
    req_data = {
        "username": "newuser",
        "password": "newusernewuser",
        "name": "New User",
        "email": "newuser@example.com",
        "staff": False,
        "admin": False,
        "enabled": True,
    }

    with (
        patch("team_copilot.routers.users.get_us", return_value=None) as mock_get_user,
        patch("team_copilot.routers.users.save_user") as mock_save_user,
    ):
        # Configure the "save_user" mock to simulate the fields of the user that are
        # generated by the database server when creating a user ("id", "created_at"
        # and "updated_at").
        def side_effect(u: User):
            u.id = user_id
            u.created_at = now
            u.updated_at = now

        mock_save_user.side_effect = side_effect

        # Make HTTP request
        response = test_client.post("/users", json=req_data)

        # Check response
        assert response.status_code == status.HTTP_201_CREATED

        res_data = response.json()
        assert len(res_data) == 2

        assert (
            res_data["message"] == f'User {user_id} ({req_data["username"]}) created.'
        )

        assert res_data["data"]["user_id"] == str(user_id)

        # Check function calls
        mock_get_user.assert_called_once_with(
            username=req_data["username"],
            email=req_data["email"],
        )

        mock_save_user.assert_called_once()

    app.dependency_overrides.clear()



def test_create_user_exists_username(
    app: FastAPI,
    test_client: TestClient,
    test_admin_user: User,
):
    """Test the "create_user" endpoint when a user with the same username already
    exists.

    Args:
        app (FastAPI): FastAPI application.
        test_client (TestClient): FastAPI test client.
        test_admin_user (User): Mock enabled administrator user.
    """
    # Simulate the injected dependency
    app.dependency_overrides[get_admin_user] = lambda: test_admin_user

    now = datetime.now(timezone.utc)

    # Simulate an existing user
    user = User(
        id=uuid4(),
        username="user1",
        password="user1user1",
        name="User 1",
        email="user1@example.com",
        staff=False,
        admin=False,
        enabled=True,
        created_at=now,
        updated_at=now,
    )

    # Request data
    req_data = {
        "username": "user1",
        "password": "user2user2",
        "name": "User 2",
        "email": "user2@example.com",
    }

    with patch("team_copilot.routers.users.get_us", return_value=user) as mock_get_user:
        # Make HTTP request
        response = test_client.post("/users", json=req_data)

        # Check response
        assert response.status_code == status.HTTP_409_CONFLICT

        res_data = response.json()
        assert len(res_data) == 3

        assert res_data["message"] == "Error."
        assert res_data["count"] == 1

        data = res_data["data"]

        assert len(data) == 1
        assert data[0]["id"] == "error"

        assert data[0]["message"] == (
            "A user with the same username or e-mail address already exists."
        )

        # Check function calls
        mock_get_user.assert_called_once_with(
            username=req_data["username"],
            email=req_data["email"],
        )



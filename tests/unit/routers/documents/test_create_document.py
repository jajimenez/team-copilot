"""Team Copilot Tests - Unit Tests - Routers - Documents - Create Document."""

from uuid import uuid4
from datetime import datetime, timezone
from io import BytesIO
from unittest.mock import patch

from fastapi import FastAPI, status
from fastapi.testclient import TestClient

from team_copilot.core.auth import get_staff_user
from team_copilot.models.data import User, Document, DocumentStatus

from tests.unit.routers import raise_not_authorized_exc


def test_create_document(
    app: FastAPI,
    test_client: TestClient, 
    staff_user_mock: User,
    pdf_file_mock: BytesIO,
):
    """Test the "create_document" endpoint.

    Args:
        app (FastAPI): FastAPI application.
        test_client (TestClient): FastAPI test client.
        staff_user_mock (User): Enabled staff user mock.
        pdf_file_mock (BytesIO): PDF file mock.
    """
    app.dependency_overrides[get_staff_user] = lambda: staff_user_mock

    doc_id = uuid4()
    doc_name = "Document"
    now = datetime.now(timezone.utc)

    # Request data
    req_data = {"name": doc_name}
    files = {"file": ("document.pdf", pdf_file_mock, "application/pdf")}

    with (
        patch(
            "team_copilot.routers.documents.get_doc",
            return_value=None,
        ) as get_doc_mock,
        patch("team_copilot.routers.documents.save_document") as save_doc_mock,
        patch(
            "team_copilot.routers.documents.get_document_file_path",
            return_value="/tmp/test.pdf",
        ) as get_path_mock,
        patch("team_copilot.routers.documents.upload_file") as upload_file_mock,
        patch("team_copilot.routers.documents.process_document") as proc_doc_mock,
    ):
        # Configure the "save_document" mock to simulate the fields of the document that
        # are generated by the database server when creating a document ("id",
        # "created_at" and "updated_at").
        def side_effect(d: Document):
            d.id = doc_id
            d.created_at = now
            d.updated_at = now

        save_doc_mock.side_effect = side_effect

        # Make HTTP request
        response = test_client.post("/documents/", data=req_data, files=files)
        
        # Check response
        assert response.status_code == status.HTTP_202_ACCEPTED

        res_data = response.json()
        assert len(res_data) == 2

        assert (
            res_data["message"] ==
            f"Document {doc_id} ({doc_name}) created and scheduled for processing."
        )

        data = res_data["data"]
        assert len(data) == 2

        assert data["document_id"] == str(doc_id)
        assert data["document_status"] == DocumentStatus.PENDING

        # Check function calls
        get_doc_mock.assert_called_once_with(name=doc_name)
        save_doc_mock.assert_called_once()
        get_path_mock.assert_called_once_with(doc_id)
        upload_file_mock.assert_called_once()
        proc_doc_mock.assert_called_once()

    app.dependency_overrides.clear()



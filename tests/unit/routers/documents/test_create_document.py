def test_create_document_exists(
    app: FastAPI,
    test_client: TestClient,
    staff_user_mock: User,
    pdf_file_mock: BytesIO,
):
    """Test the "create_document" endpoint when a document with the same name already
    exists.

    Args:
        app (FastAPI): FastAPI application.
        test_client (TestClient): FastAPI test client.
        staff_user_mock (User): Enabled staff user mock.
        pdf_file_mock (BytesIO): PDF file mock.
    """
    app.dependency_overrides[get_staff_user] = lambda: staff_user_mock

    doc_name = "Document"
    now = datetime.now(timezone.utc)

    # Simulate an existing document
    doc = Document(
        id=uuid4(),
        name=doc_name,
        created_at=now,
        updated_at=now,
    )

    # Request data
    req_data = {"name": doc_name}
    files = {"file": ("document.pdf", pdf_file_mock, "application/pdf")}

    with patch(
        "team_copilot.routers.documents.get_doc",
        return_value=doc,
    ) as get_doc_mock:
        # Make HTTP request
        response = test_client.post("/documents/", data=req_data, files=files)

        # Check response
        assert response.status_code == status.HTTP_409_CONFLICT

        res_data = response.json()
        assert len(res_data) == 3

        assert res_data["message"] == "Error."
        assert res_data["count"] == 1

        data = res_data["data"]
        assert len(data) == 1

        assert data[0]["id"] == "error"
        assert data[0]["message"] == "A document with the same name already exists."

        # Check function calls
        get_doc_mock.assert_called_once_with(name=doc_name)

    app.dependency_overrides.clear()
